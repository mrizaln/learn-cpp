/*------------------------------------------------------------------------------
              ============[ bitwise operators ]============
--------------------------------------------------------------------------------
  operator   | symbol |   form   |  operation
--------------------------------------------------------------------------------
left  shift  |   <<   |  x << y  |  shift bits in x by y times to the left
right shift  |   >>   |  x >> y  |  shift bits in x by y times to the right
bitwise NOT  |   ~    |  ~x      |  flip all bits in x
bitwise AND  |   &    |  x & y   |  each bit in x AND each bit in y
bitwise OR   |   |    |  x | y   |  each bit in x OR  each bit in y
bitwise XOR  |   ^    |  x ^ y   |  each bit in x XOR each bit in y
------------------------------------------------------------------------------*/




/*------------------------------------------------------------------------------
            ============[ bitwise assignment operators ]============
--------------------------------------------------------------------------------
        operator       | symbol |   form
--------------------------------------------------------------------------------
left  shift assignment |  <<=   |  x <<= y
right shift assignment |  >>=   |  x >>= y
bitwise NOT assignment |  NONE  |  NONE           ~ is unary, use x = ~x instead
bitwise AND assignment |  &=    |  x &= y
bitwise OR  assignment |  |=    |  x |= y
bitwise XOR assignment |  ^=    |  x ^= y
------------------------------------------------------------------------------*/

